int ContextWindow::Init()
{
	// Setup window
	glfwSetErrorCallback(ContextWindow::glfw_error_callback);
	if (!glfwInit())
		return 1;

	// Decide GL+GLSL versions
#if __APPLE__
	// GL 3.2 + GLSL 150
	const char* glsl_version = "#version 150";
	glfwWindowHint(GLFW_CONTEXT_VERSION_MAJOR, 3);
	glfwWindowHint(GLFW_CONTEXT_VERSION_MINOR, 2);
	glfwWindowHint(GLFW_OPENGL_PROFILE, GLFW_OPENGL_CORE_PROFILE);  // 3.2+ only
	glfwWindowHint(GLFW_OPENGL_FORWARD_COMPAT, GL_TRUE);            // Required on Mac
#else
	// GL 3.0 + GLSL 130
	const char* glsl_version = "#version 130";
	glfwWindowHint(GLFW_CONTEXT_VERSION_MAJOR, 3);
	glfwWindowHint(GLFW_CONTEXT_VERSION_MINOR, 0);
	//glfwWindowHint(GLFW_OPENGL_PROFILE, GLFW_OPENGL_CORE_PROFILE);  // 3.2+ only
	//glfwWindowHint(GLFW_OPENGL_FORWARD_COMPAT, GL_TRUE);            // 3.0+ only
#endif

	// Create window with graphics context
	m_window = glfwCreateWindow(MINVIEWPORTWIDTH, MINVIEWPORTHEIGHT, "InspectorCV", NULL, NULL);
	if (m_window == nullptr)
		return 1;
	//set minimum window size
	glfwSetWindowSizeLimits(m_window, MINVIEWPORTWIDTH, MINVIEWPORTHEIGHT, GLFW_DONT_CARE, GLFW_DONT_CARE);
	glfwMakeContextCurrent(m_window);
	glfwSwapInterval(1); // Enable vsync

	// Initialize OpenGL loader
#if defined(IMGUI_IMPL_OPENGL_LOADER_GL3W)
	bool err = gl3wInit() != 0;
#elif defined(IMGUI_IMPL_OPENGL_LOADER_GLEW)
	bool err = glewInit() != GLEW_OK;
#elif defined(IMGUI_IMPL_OPENGL_LOADER_GLAD)
	bool err = gladLoadGL() == 0;
#else
	bool err = false; // If you use IMGUI_IMPL_OPENGL_LOADER_CUSTOM, your loader is likely to requires some form of initialization.
#endif
	if (err)
	{
		fprintf(stderr, "Failed to initialize OpenGL loader!\n");
		return 1;
	}

	// Setup Dear ImGui context
	IMGUI_CHECKVERSION();
	ImGui::CreateContext();
	ImGuiIO& io = ImGui::GetIO(); (void)io;

	// Setup Dear ImGui style
	ActivateStyle();
    // load font
    LoadFont();

	// Setup Platform/Renderer bindings
	bool result = ImGui_ImplGlfw_InitForOpenGL(m_window, true);
	ImGui_ImplOpenGL3_Init(glsl_version);

    // create windows
    m_frameWidow = new FrameWindow("Image Viewer");

	//initial windows
	m_frameWidow->Init(this->GetTextureID());

    // load configuration
    if (DataParseUtil::DoesFileExist(m_configPath))
    {
        std::ifstream t(m_configPath);
        t.seekg(0, std::ios::end);
        size_t size = t.tellg();
        std::string config(size, ' ');
        t.seekg(0);
        t.read(&config[0], size);
        this->SetConfig(config);
    }
	return 0;
}
int ContextWindow::Run()
{
	//last window width
	int lastWindowWidth = MINVIEWPORTWIDTH;
	int lastWindowHeight = MINVIEWPORTHEIGHT;
	//setup 'enable docking' flag
	ImGuiIO& io = ImGui::GetIO();
	io.ConfigFlags |= ImGuiConfigFlags_DockingEnable;
	// Main loop
	while (!glfwWindowShouldClose(m_window))
	{
		std::chrono::time_point<std::chrono::system_clock> startTimePoint = std::chrono::system_clock::now();
		// Poll and handle events (inputs, window resize, etc.)
		// You can read the io.WantCaptureMouse, io.WantCaptureKeyboard flags to tell if dear imgui wants to use your inputs.
		// - When io.WantCaptureMouse is true, do not dispatch mouse input data to your main application.
		// - When io.WantCaptureKeyboard is true, do not dispatch keyboard input data to your main application.
		// Generally you may always pass all inputs to dear imgui, and hide them from your application based on those two flags.
		glfwPollEvents();

		// Start the Dear ImGui frame
		ImGui_ImplOpenGL3_NewFrame();
		ImGui_ImplGlfw_NewFrame();
		ImGui::NewFrame();

		//Dock space
		ImGui::DockSpaceOverViewport(ImGui::GetMainViewport());

        // Render picture viewer
        m_frameWidow->Render();

		// Rendering
		ImGui::Render();
		int display_w, display_h;
		glfwMakeContextCurrent(m_window);
		glfwGetFramebufferSize(m_window, &display_w, &display_h);
		glViewport(0, 0, display_w, display_h);
		glClearColor(m_backgroundColor.x, m_backgroundColor.y, m_backgroundColor.z, m_backgroundColor.w);
		glClear(GL_COLOR_BUFFER_BIT);
		ImGui_ImplOpenGL3_RenderDrawData(ImGui::GetDrawData());

		glfwMakeContextCurrent(m_window);
		glfwSwapBuffers(m_window);
	}
	return 0;
}

void ContextWindow::Deinit()
{
    // Get configs and save
    std::string config = this->GetConfig();
    // save
    std::ofstream configFile("config.json");
    configFile << config;
    configFile.close();
    // picture window
    m_frameWidow->Deinit();
    delete m_frameWidow;
	// Cleanup
	ImGui_ImplOpenGL3_Shutdown();
	ImGui_ImplGlfw_Shutdown();
	ImGui::DestroyContext();
	glfwDestroyWindow(m_window);
	glfwTerminate();
}

