bool ContextWindow::LoadFont(std::string path)
{
    // check if path to font exist
    std::string executablePath = DataParseUtil::GetExecutablePath();
    std::string fullFontPath = DataParseUtil::GetFileDirectory(executablePath) + "//" + path;
    if (DataParseUtil::DoesFileExist(fullFontPath))
    {
        ImGuiIO& io = ImGui::GetIO();
        io.Fonts->AddFontFromFileTTF(fullFontPath.c_str(), 14);
        return true;
    }
    else
    {
        std::cout << "Font resource in path: " << path << " doesn't exist!" << std::endl;
        return false;
    }
}

std::string ContextWindow::GetConfig()
{
    nlohmann::json config;
    // sparately get picture view window configuration
    std::string window_config = m_frameWidow->GetConfig();
    std::string window_name = m_frameWidow->GetName();
    config[window_name] = window_config;

    return config.dump();
}

void ContextWindow::SetConfig(std::string& config)
{
    nlohmann::json configJSON;
    try
    {
        configJSON = nlohmann::json::parse(config);
    }
    catch (...)
    {
        std::cout << "Failed to parse configuration from " << m_configPath << std::endl;
    }
    if (configJSON.empty())
    {
        return;
    }
}
